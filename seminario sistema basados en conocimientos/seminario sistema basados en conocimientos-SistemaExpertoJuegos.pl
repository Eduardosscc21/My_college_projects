si_te_gusta(JuegoBase, JuegoRecomendado) :-
    setof(J, juego_con_generos_comunes(JuegoBase, J), JuegosUnicos),
    member(JuegoRecomendado, JuegosUnicos).

juego_con_generos_comunes(JuegoBase, JuegoRecomendado) :-
    juego(JuegoRecomendado),
    JuegoBase \= JuegoRecomendado,
    genero(JuegoBase, Genero),
    genero(JuegoRecomendado, Genero).
