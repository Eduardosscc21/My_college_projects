alternativa_gratis(JuegoPago, JuegoGratis) :-
    precio(JuegoPago, Precio), Precio \= 'Gratis',
    precio(JuegoGratis, 'Gratis'),
    si_te_gusta(JuegoPago, JuegoGratis).

