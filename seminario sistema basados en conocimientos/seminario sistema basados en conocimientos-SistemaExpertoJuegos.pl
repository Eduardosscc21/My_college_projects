%Reglas
juego_ideal(Preferencias, JuegoRecomendado) :-
    juego(JuegoRecomendado),
    cumple_preferencias(JuegoRecomendado, Preferencias).

cumple_preferencias(Juego, Preferencias) :-
    forall(member(Preferencia, Preferencias), cumple_preferencia(Juego, Preferencia)).

cumple_preferencia(Juego, gratis) :- precio(Juego, 'Gratis').
cumple_preferencia(Juego, de_pago) :- precio(Juego, Precio), Precio \= 'Gratis'.
cumple_preferencia(Juego, economico) :-
    precio(Juego, Precio), Precio \= 'Gratis', atom_number(Precio, N), N < 1000.
cumple_preferencia(Juego, precio_medio) :-
    precio(Juego, Precio), Precio \= 'Gratis', atom_number(Precio, N), N >= 1000, N =< 2500.
cumple_preferencia(Juego, premium) :-
    precio(Juego, Precio), Precio \= 'Gratis', atom_number(Precio, N), N > 2500.

cumple_preferencia(Juego, multijugador) :- categoria(Juego, 'Multiplayer').
cumple_preferencia(Juego, un_jugador) :- categoria(Juego, 'Singleplayer').
cumple_preferencia(Juego, cooperativo) :-
    categoria(Juego, 'Coop'); categoria(Juego, 'OnlineCoop').
cumple_preferencia(Juego, competitivo) :-
    categoria(Juego, 'PvP'); categoria(Juego, 'OnlinePvP').

cumple_preferencia(Juego, accion) :- genero(Juego, 'Action').
cumple_preferencia(Juego, aventura) :- genero(Juego, 'Adventure').
cumple_preferencia(Juego, rpg) :- genero(Juego, 'RPG').
cumple_preferencia(Juego, estrategia) :- genero(Juego, 'Strategy').
cumple_preferencia(Juego, deportes) :- genero(Juego, 'Sports').
cumple_preferencia(Juego, carreras) :- genero(Juego, 'Racing').
cumple_preferencia(Juego, simulacion) :- genero(Juego, 'Simulation').
cumple_preferencia(Juego, indie) :- genero(Juego, 'Indie').
cumple_preferencia(Juego, casual) :- genero(Juego, 'Casual').
cumple_preferencia(Juego, horror) :- genero(Juego, 'Horror').

cumple_preferencia(Juego, con_control) :-
    categoria(Juego, 'FullControllerSupport'); categoria(Juego, 'PartialControllerSupport').
cumple_preferencia(Juego, con_logros) :- categoria(Juego, 'SteamAchievements').
cumple_preferencia(Juego, mods) :- categoria(Juego, 'SteamWorkshop').
cumple_preferencia(Juego, crossplay) :- categoria(Juego, 'CrossPlatformMultiplayer').
